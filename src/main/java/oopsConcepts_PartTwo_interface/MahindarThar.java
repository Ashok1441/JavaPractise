package oopsConcepts_PartTwo_interface;
/*
 * Child class or sub class
 */
public class MahindarThar extends Car {

	
	public void carBrand() {
		System.out.println("Car Brand is Mahindra Thar");
	}
	//overridden class
	public void start() {
		System.out.println("Thar--------Started");
	}
	//overridden class
	public void seatingCapcity() {
		System.out.println("Car Seating Capacity is 4");
	}
	//overridden class
	public void carColor() {
		System.out.println("Car Color is black");
	}
}
